#include "ft_printf.h"

void	ft_argputnbr(long n, va_list args)
{
	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
	}
	if (n < 0 && n != -2147483648)
	{
		ft_putchar('-');
		n = n * -1;
	}
	if (n <= 9 && n >= 0)
	{
		ft_putchar((n + 48));
	}
	if (n >= 10)
	{
		ft_argputnbr((n / 10), args);
		ft_argputnbr((n % 10), args);
	}
}
